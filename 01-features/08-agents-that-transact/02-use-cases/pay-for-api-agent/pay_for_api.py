    print("\n✅ AgentCore Payments resources cleaned up. .env cleared of provisioned IDs.")

    # ── Tear down the CDK stacks ────────────────────────────────────────────
    here = pathlib.Path(__file__).resolve().parent
    _run_destroy_script(here, "destroy-seller.sh", "Fun Facts seller stack")
