def _wait_for_port(port: int, host: str = "127.0.0.1", timeout: int = 60) -> bool:
    """Block until a TCP port accepts connections, or timeout. True on success."""
    import socket

    deadline = time.time() + timeout
    while time.time() < deadline:
        try:
            with socket.create_connection((host, port), timeout=1):
                return True
        except OSError:
            time.sleep(0.5)
    return False


