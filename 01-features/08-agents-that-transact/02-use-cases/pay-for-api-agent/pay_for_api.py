def _run_destroy_script(here: pathlib.Path, script_name: str, label: str) -> None:
    """Run a destroy-*.sh script under test/integration/ if it exists."""
    script = here / "test" / "integration" / script_name
    if not script.exists():
        print(f"\n  ↷ {label}: {script_name} not found, skipping")
        return
    print(f"\n=== Tearing down {label} via test/integration/{script_name} ===")
    proc = subprocess.run(["bash", str(script)], cwd=str(here))
    if proc.returncode != 0:
        # Don't sys.exit — keep teardown going. The user can re-run if needed.
        print(f"  ⚠️  {script_name} exited {proc.returncode}; continuing with remaining cleanup")


def _remove_local_artifacts(here: pathlib.Path) -> None:
    """Remove local CDK build artifacts and runtime caches.

    Matches the notebook's §72 cleanup. None of these hold cloud state,
    so clearing them is safe and keeps the repo clean for the next run.
    """
    import shutil

    print("\n=== Removing local build artifacts ===")
    targets = [
        # Seller CDK
        here / "seller" / "cdk" / ".venv",
        here / "seller" / "cdk" / "cdk.out",
        here / "seller" / "cdk" / "__pycache__",
        here / "seller" / "cdk" / "outputs.json",
        # Seller Lambda npm modules (~16k files of x402 facilitator deps)
        here / "seller" / "lambda" / "node_modules",
        here / "seller" / "lambda" / "package-lock.json",
        # Agent CDK
        here / "agent" / "cdk" / ".venv",
        here / "agent" / "cdk" / "cdk.out",
        here / "agent" / "cdk" / "__pycache__",
        here / "agent" / "cdk" / "outputs.json",
        # Use-case-root caches
        here / "__pycache__",
        here / "test" / "integration" / "__pycache__",
        # Privy Wallet Hub frontend (cloned at runtime by §6b)
        here / "privy-delegation",
        # Use-case-root venv and env file. Removed last so a re-run starts
        # from a clean slate; gitignored so they would never be committed,
        # but we delete them anyway to keep the working tree tidy.
        here / ".venv",
        here / ".env",
    ]

    for path in targets:
        if not path.exists():
            print(f"  ↷ skip (absent): {path.relative_to(here)}")
            continue
        try:
            if path.is_dir():
                shutil.rmtree(path)
            else:
                path.unlink()
            print(f"  🗑️  removed: {path.relative_to(here)}")
        except OSError as exc:
            print(f"  ⚠️  could not remove {path.relative_to(here)}: {exc}")


