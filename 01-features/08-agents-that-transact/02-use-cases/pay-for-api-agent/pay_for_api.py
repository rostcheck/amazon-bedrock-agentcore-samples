def _check_secret(label: str, value: str, optional: bool = False) -> bool:
    """Pretty-print a sensitive env-var check (e.g., API keys, wallet secrets).

    Sensitive values are NEVER interpolated into stdout. This function uses
    fixed status placeholders only ("[redacted]" / "(unset)" / "(will be
    set later)"), so secret values cannot leak through print(). Callers
    pass redacted values here; non-secrets go to _check_plain() instead.
    """
    ok = bool(value) and not value.startswith("<")
    if optional and not ok:
        print(f"  ⏳  {label}: (will be set later)")
        return True
    if ok:
        print(f"  ✅  {label}: [redacted]")
    else:
        print(f"  ❌ MISSING  {label}: (unset)")
    return ok


def _check_plain(label: str, value: str, optional: bool = False) -> bool:
    """Pretty-print a non-sensitive env-var check (e.g., region, ARN, ID, email).

    Values printed here are non-secrets by contract: AWS region, IAM role
    ARNs, Manager / Connector / Instrument / Session IDs, wallet addresses,
    and the linked email. Variables whose names match CodeQL's sensitivity
    heuristic (e.g., contain `KEY` or `AUTH`) must use _check_secret()
    even when their values are technically public, to keep the static
    analyzer quiet.
    """
    ok = bool(value) and not value.startswith("<")
    display = value
    if optional and not ok:
        display = display or "(will be set later)"
        print(f"  ⏳  {label}: {display}")
        return True
    icon = "✅" if ok else "❌ MISSING"
    print(f"  {icon}  {label}: {display}")
    return ok


