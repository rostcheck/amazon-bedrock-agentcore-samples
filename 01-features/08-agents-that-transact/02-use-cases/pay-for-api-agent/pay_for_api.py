"""Pay-for-API — Strands buyer agent that pays autonomously for a metered HTTP API.

App-backend script that:
  1. Installs the Python dependencies (handled out of band by the user)
  2. Loads .env (USER_ID auto-generated by setup-env.sh on first run)
  3. Deploys the Fun Facts seller stack (CDK -> API Gateway + Lambda + x402)
       (auto-runs deploy-seller.sh on first run; idempotent thereafter)
  4. Provisions the AgentCore Payments stack:
       Two Credential Providers (Coinbase CDP + Stripe via Privy)
       -> one Manager
       -> two Connectors (CDP + Privy)
       -> four Instruments (EVM + Solana per provider)
  5. Creates two payment sessions (one per provider) with spend caps
  6. Grants signing delegation:
       - Opens the Coinbase Wallet Hub in the user's browser
       - Clones, installs, and runs the Privy delegation Next.js frontend
         on http://localhost:3000 and waits for the user to grant access
       Press Enter in this terminal once each hub confirms delegation.
  7. Runs the agent four times locally (provider x network matrix)
  8. Deploys the agent to AgentCore Runtime via CodeBuild and invokes it
       (interactive prompt: Enter to deploy, q to skip)
  9. Inspects the data plane (sessions, balances, lists)
 10. Cleanup (revoke sessions, destroy stacks, delete payments resources)
       (interactive prompt: Enter to clean up, q to skip)

Usage:
    python pay_for_api.py

Prerequisites:
    bash test/integration/setup-roles.sh      # creates four IAM roles
    cp env-sample.txt .env                    # then fill in wallet provider secrets
    bash test/integration/setup-env.sh        # generates USER_ID

Notebook companion: see the matching pay-for-api.ipynb under
06-workshops/13-AgentCore-payments/02-use-cases/pay-for-api-agent/ for a
step-by-step walkthrough with markdown commentary and screenshots.

The agent is designed so that it never holds private keys.
AgentCore Identity stores wallet provider secrets in AWS Secrets Manager
under KMS encryption after CreatePaymentCredentialProvider runs in §4.
The agent runtime calls GetResourcePaymentToken at signing time and
never receives the raw secret.
"""

