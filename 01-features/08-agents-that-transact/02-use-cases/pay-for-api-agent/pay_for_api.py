def build_agent(
    *,
    clients: dict,
    instrument_id: str,
    session_id: str,
    network_preferences: list[str],
    label: str,
):
    """Build a Strands Agent with the AgentCorePaymentsPlugin attached.

    `network_preferences` is a CAIP-style priority list (e.g.
    ["base-sepolia", "solana-devnet"]) telling the plugin which entry
    in the seller's 402 accepts[] to match against the instrument.
    """
    from strands import Agent
    from strands.models.bedrock import BedrockModel
    from strands_tools import http_request
    from bedrock_agentcore.payments.integrations.config import (
        AgentCorePaymentsPluginConfig,
    )
    from bedrock_agentcore.payments.integrations.strands.plugin import (
        AgentCorePaymentsPlugin,
    )

    model = BedrockModel(
        model_id="us.anthropic.claude-sonnet-4-5-20250929-v1:0",
        region_name=AWS_REGION,
        temperature=0.7,
    )

    payment_user_id = resolve_payment_user_id(clients, instrument_id)

    plugin_cfg = AgentCorePaymentsPluginConfig(
        payment_manager_arn=MANAGER_ARN,
        user_id=payment_user_id,
        payment_instrument_id=instrument_id,
        payment_session_id=session_id,
        region=AWS_REGION,
        agent_name=f"pay-for-api-agent-{label.lower().replace(' ', '-')}",
        network_preferences_config=network_preferences,
    )
    plugin = AgentCorePaymentsPlugin(config=plugin_cfg)

    return Agent(
        model=model,
        tools=[http_request],
        system_prompt=SYSTEM_PROMPT,
        plugins=[plugin],
    )


def resolve_payment_user_id(clients: dict, instrument_id: str) -> str:
    """Read paymentInstrument.userId — the vendor-level ID payment ops must run under."""
