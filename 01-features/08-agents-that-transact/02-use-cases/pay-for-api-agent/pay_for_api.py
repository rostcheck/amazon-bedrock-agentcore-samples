def _confirm_section(title: str, body_lines: list[str], action_label: str) -> bool:
    """Show a framed banner + body, then prompt: Enter to run, q + Enter to skip."""
    print("\n" + "=" * 72)
    print(f"  {title}")
    print("=" * 72)
    print()
    for line in body_lines:
        print(line)
    print()
    print(f"  ▶▶ Press Enter to {action_label}, or press q + Enter to skip.")
    choice = input("     [Enter to continue | q to skip] ").strip().lower()
    return choice not in ("q", "quit", "skip", "n", "no")


# ── §3. Provision and sanity-check the seller ───────────────────────────────
def deploy_seller_if_needed() -> None:
    """Run deploy-seller.sh and reload .env if SELLER_API_URL is unset."""
    global SELLER_API_URL
    if SELLER_API_URL:
        print(f"\n↷ Seller already deployed ({SELLER_API_URL}), skipping seller deploy.")
        return

    print("\n=== §3. Deploy the Fun Facts seller via test/integration/deploy-seller.sh ===")
    here = pathlib.Path(__file__).resolve().parent
    proc = subprocess.run(
        ["bash", "test/integration/deploy-seller.sh"],
        cwd=here,
    )
    if proc.returncode != 0:
        sys.exit(f"deploy-seller.sh exited {proc.returncode}")

    # Reload .env to pick up the SELLER_API_URL the script just wrote.
    load_dotenv(override=True)
    SELLER_API_URL = os.environ.get("SELLER_API_URL", "").rstrip("/")
    if not SELLER_API_URL:
        sys.exit("deploy-seller.sh finished but SELLER_API_URL was not written to .env.")
    print(f"\n✅ Seller URL: {SELLER_API_URL}")


def sanity_check_seller() -> None:
    if not SELLER_API_URL:
        sys.exit("SELLER_API_URL is unset — deploy_seller_if_needed() should have populated it.")

    print(f"\n=== §3. Sanity-check the seller — {SELLER_API_URL} ===")
    health = requests.get(f"{SELLER_API_URL}/health", timeout=10)
    health.raise_for_status()
    print(f"Health: {health.json()}")

    # Preview the 402 response — what the agent will see on its first request
    preview = requests.get(f"{SELLER_API_URL}/facts?topic=space", timeout=10)
    print(f"Preview /facts -> HTTP {preview.status_code}")
    if preview.status_code == 402:
        body = preview.json()
        accepts = body.get("accepts") or []
        print(f"x402Version: {body.get('x402Version')}, accepts ({len(accepts)}):")
        for a in accepts:
            print(f"   network={a.get('network')}, payTo={a.get('payTo')}, price={a.get('maxAmountRequired')}")


# ── §4.1 Assume roles + build clients ────────────────────────────────────────
def build_clients() -> dict:
    missing = [
        n
        for n, v in (
            ("CONTROL_PLANE_ROLE_ARN", CONTROL_PLANE_ROLE_ARN),
            ("MANAGEMENT_ROLE_ARN", MANAGEMENT_ROLE_ARN),
            ("RESOURCE_RETRIEVAL_ROLE_ARN", RESOURCE_RETRIEVAL_ROLE_ARN),
        )
        if not v
    ]
    if missing:
        sys.exit(f"Missing IAM roles: {missing}. Run bash test/integration/setup-roles.sh.")

    boto_session = Session(region_name=AWS_REGION)
    print("Assuming ControlPlaneRole...")
    cp_session = assume_role(boto_session, CONTROL_PLANE_ROLE_ARN, "pay-for-api-cp")
    cp_client = cp_session.client("bedrock-agentcore-control")
    cred_client = cp_session.client("bedrock-agentcore-control")

    print("Assuming ManagementRole...")
    mgmt_session = assume_role(boto_session, MANAGEMENT_ROLE_ARN, "pay-for-api-mgmt")
    dp_client_mgmt = mgmt_session.client("bedrock-agentcore")

    print("Assuming ProcessPaymentRole...")
    pp_session = assume_role(boto_session, PROCESS_PAYMENT_ROLE_ARN, "pay-for-api-pp")
    dp_client_pp = pp_session.client("bedrock-agentcore")

    return {
        "cp": cp_client,
        "cred": cred_client,
        "dp_mgmt": dp_client_mgmt,
        "dp_pp": dp_client_pp,
    }


# ── §4.2 Create the Credential Providers ─────────────────────────────────────
# PaymentCredentialProvider is the security handoff. AgentCore Identity stores
# wallet provider secrets in AWS Secrets Manager under KMS encryption and
# surfaces only the secret ARN. The agent runtime never receives the raw secret;
# at signing time it calls GetResourcePaymentToken to obtain a short-lived
# vendor-specific token. After this step runs, the local .env copies of these
# secrets are no longer needed at runtime.
def create_credential_providers(clients: dict) -> tuple[str, str]:
    global CREDENTIAL_PROVIDER_ARN_CDP, CREDENTIAL_PROVIDER_ARN_PRIVY
    cred = clients["cred"]

    # CDP
    if CREDENTIAL_PROVIDER_ARN_CDP:
        print("↷ CREDENTIAL_PROVIDER_ARN_CDP already set, skipping CDP cred provider create")
    else:
        missing_cdp = [
            n
            for n, v in (
                ("COINBASE_API_KEY_ID", COINBASE_API_KEY_ID),
                ("COINBASE_API_KEY_SECRET", COINBASE_API_KEY_SECRET),
                ("COINBASE_WALLET_SECRET", COINBASE_WALLET_SECRET),
            )
            if not v
        ]
        if missing_cdp:
            sys.exit(f"Missing Coinbase secrets in .env: {missing_cdp}")

        cred_name = f"PayForApiCDPCred{uuid.uuid4().hex[:6]}"
        resp = cred.create_payment_credential_provider(
            name=cred_name,
            credentialProviderVendor="CoinbaseCDP",
            providerConfigurationInput={
                "coinbaseCdpConfiguration": {
                    "apiKeyId": COINBASE_API_KEY_ID,
                    "apiKeySecret": COINBASE_API_KEY_SECRET,
                    "walletSecret": COINBASE_WALLET_SECRET,
                }
            },
        )
        CREDENTIAL_PROVIDER_ARN_CDP = resp["credentialProviderArn"]
        write_env_updates(
            {
                "CREDENTIAL_PROVIDER_ARN_CDP": CREDENTIAL_PROVIDER_ARN_CDP,
                "CRED_PROVIDER_NAME_CDP": cred_name,
            }
        )
        print(f"✅ CDP cred provider: {CREDENTIAL_PROVIDER_ARN_CDP}")

    # Privy
    if CREDENTIAL_PROVIDER_ARN_PRIVY:
        print("↷ CREDENTIAL_PROVIDER_ARN_PRIVY already set, skipping Privy cred provider create")
    else:
        missing_privy = [
            n
            for n, v in (
                ("PRIVY_APP_ID", PRIVY_APP_ID),
                ("PRIVY_APP_SECRET", PRIVY_APP_SECRET),
                ("PRIVY_AUTHORIZATION_ID", PRIVY_AUTHORIZATION_ID),
                ("PRIVY_AUTHORIZATION_PRIVATE_KEY", PRIVY_AUTHORIZATION_PRIVATE_KEY),
            )
            if not v
        ]
        if missing_privy:
            print(
                f"↷ Privy values missing from .env ({len(missing_privy)} key(s)), skipping Privy cred provider create"
            )
        else:
            cred_name = f"PayForApiPrivyCred{uuid.uuid4().hex[:6]}"
            resp = cred.create_payment_credential_provider(
                name=cred_name,
                credentialProviderVendor="StripePrivy",
                providerConfigurationInput={
                    "stripePrivyConfiguration": {
                        "appId": PRIVY_APP_ID,
                        "appSecret": PRIVY_APP_SECRET,
                        "authorizationId": PRIVY_AUTHORIZATION_ID,
                        "authorizationPrivateKey": PRIVY_AUTHORIZATION_PRIVATE_KEY,
                    }
                },
            )
            CREDENTIAL_PROVIDER_ARN_PRIVY = resp["credentialProviderArn"]
            write_env_updates(
                {
                    "CREDENTIAL_PROVIDER_ARN_PRIVY": CREDENTIAL_PROVIDER_ARN_PRIVY,
                    "CRED_PROVIDER_NAME_PRIVY": cred_name,
                }
            )
            print(f"✅ Privy cred provider: {CREDENTIAL_PROVIDER_ARN_PRIVY}")

    return CREDENTIAL_PROVIDER_ARN_CDP, CREDENTIAL_PROVIDER_ARN_PRIVY


# ── §4.3 Create the Payment Manager ──────────────────────────────────────────
# One Manager hosts both connectors so a single audit trail covers the whole
# use case. wait_for_status polls until the Manager is READY. The Manager
# stores only the ResourceRetrievalRole; ManagementRole and
# ProcessPaymentRole are session-time roles assumed by the caller.
def create_manager(clients: dict) -> tuple[str, str]:
    global MANAGER_ARN, MANAGER_ID
    cp = clients["cp"]

    if MANAGER_ARN:
        print("↷ MANAGER_ARN already set, skipping manager create")
        return MANAGER_ARN, MANAGER_ID

    # PaymentManager names: letters + digits only, max 48 chars.
    name = f"PayForApi{uuid.uuid4().hex[:8]}"
    resp = cp.create_payment_manager(
        name=name,
        description=f"AgentCore Payments Pay for API use case {name}",
        authorizerType="AWS_IAM",
        roleArn=RESOURCE_RETRIEVAL_ROLE_ARN,
        clientToken=_client_token(),
    )
    MANAGER_ID = resp["paymentManagerId"]
    MANAGER_ARN = resp["paymentManagerArn"]
    print(f"Created Manager {MANAGER_ID}, waiting for READY...")
    wait_for_status(
        cp.get_payment_manager,
        expected_status="READY",
        paymentManagerId=MANAGER_ID,
    )
    write_env_updates({"MANAGER_ARN": MANAGER_ARN, "MANAGER_ID": MANAGER_ID})
    print(f"✅ Manager: {MANAGER_ARN}")
    return MANAGER_ARN, MANAGER_ID


# ── §4.4 Create the Payment Connectors ───────────────────────────────────────
# Two connectors under the same Manager. Each binds to one credential provider.
def create_connectors(clients: dict) -> tuple[str, str]:
    global CONNECTOR_ID_CDP, CONNECTOR_ID_PRIVY
    cp = clients["cp"]

    if CONNECTOR_ID_CDP:
        print("↷ CONNECTOR_ID_CDP already set, skipping CDP connector create")
    else:
        name = f"PayForApiCDPConn{uuid.uuid4().hex[:6]}"
        resp = cp.create_payment_connector(
            paymentManagerId=MANAGER_ID,
            name=name,
            description=f"AgentCore Payments CoinbaseCDP connector {name}",
            type="CoinbaseCDP",
            credentialProviderConfigurations=[{"coinbaseCDP": {"credentialProviderArn": CREDENTIAL_PROVIDER_ARN_CDP}}],
            clientToken=_client_token(),
        )
        CONNECTOR_ID_CDP = resp["paymentConnectorId"]
        wait_for_status(
            cp.get_payment_connector,
            expected_status="READY",
            paymentManagerId=MANAGER_ID,
            paymentConnectorId=CONNECTOR_ID_CDP,
        )
        write_env_updates({"CONNECTOR_ID_CDP": CONNECTOR_ID_CDP})
        print(f"✅ CDP connector: {CONNECTOR_ID_CDP}")

    if CONNECTOR_ID_PRIVY:
        print("↷ CONNECTOR_ID_PRIVY already set, skipping Privy connector create")
    elif not CREDENTIAL_PROVIDER_ARN_PRIVY:
        print("↷ No Privy credential provider — skipping Privy connector create")
    else:
        name = f"PayForApiPrivyConn{uuid.uuid4().hex[:6]}"
        resp = cp.create_payment_connector(
            paymentManagerId=MANAGER_ID,
            name=name,
            description=f"AgentCore Payments StripePrivy connector {name}",
            type="StripePrivy",
            credentialProviderConfigurations=[
                {"stripePrivy": {"credentialProviderArn": CREDENTIAL_PROVIDER_ARN_PRIVY}}
            ],
            clientToken=_client_token(),
        )
        CONNECTOR_ID_PRIVY = resp["paymentConnectorId"]
        wait_for_status(
            cp.get_payment_connector,
            expected_status="READY",
            paymentManagerId=MANAGER_ID,
            paymentConnectorId=CONNECTOR_ID_PRIVY,
        )
        write_env_updates({"CONNECTOR_ID_PRIVY": CONNECTOR_ID_PRIVY})
        print(f"✅ Privy connector: {CONNECTOR_ID_PRIVY}")

    return CONNECTOR_ID_CDP, CONNECTOR_ID_PRIVY


# ── §4.5 Create four Payment Instruments ─────────────────────────────────────
# Two per connector: ETHEREUM (Base Sepolia) + SOLANA (Solana Devnet).
def create_instrument(
    clients: dict,
    *,
    connector_id: str,
    network: str,
    env_key_id: str,
    env_key_wallet: str,
    label: str,
) -> tuple[str, str, str]:
    dp = clients["dp_mgmt"]
    existing = os.environ.get(env_key_id, "")
    if existing:
        print(f"↷ {env_key_id} already set, skipping {label} instrument create")
        return existing, os.environ.get(env_key_wallet, ""), ""

    resp = dp.create_payment_instrument(
        paymentManagerArn=MANAGER_ARN,
        paymentConnectorId=connector_id,
        userId=USER_ID,
        paymentInstrumentType="EMBEDDED_CRYPTO_WALLET",
        paymentInstrumentDetails={
            "embeddedCryptoWallet": {
                "network": network,
                "linkedAccounts": [{"email": {"emailAddress": INSTRUMENT_EMAIL}}],
            }
        },
        clientToken=_client_token(),
    )
    instrument = resp["paymentInstrument"]
    instrument_id = instrument["paymentInstrumentId"]
    crypto = instrument["paymentInstrumentDetails"]["embeddedCryptoWallet"]
    wallet_address = crypto.get("walletAddress", "")
